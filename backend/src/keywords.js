/**
 * Keyword map — พิมพ์คำไหนได้คำตอบอะไร
 * รองรับหลาย keyword ต่อ 1 คำตอบ
 */
const KEYWORDS = {
  // ผดร้อน
  ผดร้อน: replyPricklyHeat,
  ผื่นร้อน: replyPricklyHeat,
  ตุ่มแดง: replyPricklyHeat,

  // ทารกหนาว
  หนาว: replyCold,
  ลูกหนาว: replyCold,
  ทารกหนาว: replyCold,

  // ลูกร้องไห้
  ลูกร้อง: replyCrying,
  ร้องไห้: replyCrying,
  ร้องมาก: replyCrying,
  ร้องไม่หยุด: replyCrying,

  // ดื่มน้ำ
  ดื่มน้ำ: replyWater,
  น้ำ: replyWater,
  กินน้ำ: replyWater,

  // wonder week
  'wonder week': replyWonderWeek,
  wonderweek: replyWonderWeek,
  วันเดอร์วีค: replyWonderWeek,
  งอแง: replyWonderWeek,

  // ลูกไม่กินข้าว
  ไม่กินข้าว: replyNotEating,
  ไม่ยอมกิน: replyNotEating,
  ไม่กินนม: replyNotEating,

  // นอนไม่หลับ
  นอนไม่หลับ: replySleep,
  ไม่ยอมนอน: replySleep,
  นอนยาก: replySleep,

  // ไข้
  ไข้: replyFever,
  ตัวร้อน: replyFever,
  มีไข้: replyFever,
};

/**
 * ค้นหา keyword ในข้อความที่แม่พิมพ์มา
 */
function findKeyword(text) {
  const normalized = text.toLowerCase().replace(/\s+/g, '');
  for (const [keyword, handler] of Object.entries(KEYWORDS)) {
    if (normalized.includes(keyword.toLowerCase().replace(/\s+/g, ''))) {
      return handler;
    }
  }
  return null;
}

